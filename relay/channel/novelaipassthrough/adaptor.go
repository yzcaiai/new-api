type Adaptor struct{}

func (a *Adaptor) Init(info *relaycommon.RelayInfo) {}

func (a *Adaptor) GetRequestURL(info *relaycommon.RelayInfo) (string, error) {
	if info.RelayMode != relayconstant.RelayModeChatCompletions {
		return "", errors.New("novelaipassthrough channel: endpoint not supported")
	}
	return fmt.Sprintf("%s/v1/chat/completions", info.ChannelBaseUrl), nil
}

func (a *Adaptor) SetupRequestHeader(c *gin.Context, req *http.Header, info *relaycommon.RelayInfo) error {
	// DoApiRequest passes a copy of req.Header into this function.
	// We must NOT replace the header map (i.e. no "*req = make(http.Header)")
	// otherwise changes won't be reflected on the real http.Request.
	for k := range *req {
		delete(*req, k)
	}
	req.Set("Content-Type", "application/json")
	req.Set("Authorization", "Bearer "+info.ApiKey)
	return nil
}

